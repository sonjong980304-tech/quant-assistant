def market_from_corp_cls(cls: str | None) -> str:
    """corp_cls(법인구분 코드) → 시장명. 매핑 실패 시 빈 문자열."""
    if not cls:
        return ""
    return _MARKET_MAP.get(str(cls).strip().upper(), "")


def sector_from_induty(induty_code: str | None) -> str:
    """induty_code(KSIC 기반 업종코드) → 한국어 업종명.

    앞 3자리(소분류) 우선 매핑 후 앞 2자리(중분류)로 폴백한다.
    둘 다 실패하면 코드 원본을 그대로 반환(빈 값이면 "기타").
    """
    if not induty_code:
        return "기타"
    code = str(induty_code).strip()
    if not code:
        return "기타"
    # 1차: 앞 3자리(소분류)
    if len(code) >= 3 and code[:3] in _KSIC3_MAP:
        return _KSIC3_MAP[code[:3]]
    # 2차: 앞 2자리(중분류)
    if len(code) >= 2 and code[:2] in _KSIC2_MAP:
        return _KSIC2_MAP[code[:2]]
    # 매핑 실패: 코드 원본 유지
    return code
